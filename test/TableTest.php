<?php
//----------------------------------------------------------------------------------------------------------------------
class TableTest extends DataLayerTestCase
{
  //--------------------------------------------------------------------------------------------------------------------
  /**
   * Stored routine with designation type table must show table.
   */
  public function test1()
  {

    $template_table =  "\n";
    $template_table .= "+---------+---------+---------+---------+---------------------+------+------+\n";
    $template_table .= "| tst_c00 | tst_c01 | tst_c02 | tst_c03 |       tst_c04       |  t   |  s   |\n";
    $template_table .= "+---------+---------+---------+---------+---------------------+------+------+\n";
    $template_table .= "  Hello           1     0.543   1.23450   2014-03-27 00:00:00   4444      1  \n";
    $template_table .= "  World           3   0.00003   0.00000   2014-03-28 00:00:00             1  \n";
    $template_table .= "+---------+---------+---------+---------+---------------------+------+------+\n";

    ob_start();
    DataLayer::testTable();
    $table = ob_get_contents();
    ob_end_clean();

    echo $table;

    $this->assertEquals( $table, $template_table );
  }

  //--------------------------------------------------------------------------------------------------------------------
}

